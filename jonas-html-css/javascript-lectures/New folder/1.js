function describeCountry(country, population, capitalcity) {
    return (`${country} has ${population} million people and its capital city is ${capitalcity} `)
}
console.log(describeCountry('Iran', 100, 'Tehran'))
console.log(describeCountry('Turkey', 200, 'Istanbul'))
console.log(describeCountry('Germany', 300, 'Berlin'))